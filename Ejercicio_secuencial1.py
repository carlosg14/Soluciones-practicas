"""EJERCICIO 1: Un estudiante está cursando 5 materias, tiene la nota de cada

materia y quiere saber cuál es su promedio."""


numero1 = int (input ("ingrese la primer nota: "))
numero2 = int (input ("ingrese la segunda nota: "))
numero3 = int (input ("ingrese la tercera nota: "))
numero4 = int (input ("ingrese la cuarta nota: "))
numero5 = int (input ("ingrese la quinta nota: "))


suma = numero1 + numero2 + numero3 + numero4 + numero5

promedio= suma / 5

print ("el promedio de sus 5 notas es:", promedio)


